    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        ElementBiblioteca el;
        if (Elements.size() > 0 && jComboBox2.getName() != "") {
            if (jCheckBox1.isSelected()) {
                el = new ElementBiblioteca(jLabel3.getText(), Elements.get(jComboBox2.getSelectedIndex()).getCodi(), jTextField5.getText(), jTextField6.getText(), jTextField8.getText(), Integer.parseInt(jTextField3.getText()), DateFormat.getDateInstance().format(jDateChooser1.getDate()), jTextField2.getText(), jCheckBox1.isSelected(), jCheckBox2.isSelected(), jTextField1.getText());
            } else {
                el = new ElementBiblioteca(jLabel3.getText(), Elements.get(jComboBox2.getSelectedIndex()).getCodi(), jTextField5.getText(), jTextField6.getText(), jTextField8.getText(), Integer.parseInt(jTextField3.getText()), "BUIT", jTextField2.getText(), jCheckBox1.isSelected(), jCheckBox2.isSelected(), jTextField1.getText());
            }
            try {
                b.Update(null, el);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Biblioteca.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InstantiationException ex) {
                Logger.getLogger(Biblioteca.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IllegalAccessException ex) {
                Logger.getLogger(Biblioteca.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            jLabel3.setText("No es pot editar si no tens res seleccionat");
        }
    }//GEN-LAST:event_SaveActionPerformed
    public int daysBetween(Date d1, Date d2) {
        return (int) ((d2.getTime() - d1.getTime()) / (1000 * 60 * 60 * 24));
    }
